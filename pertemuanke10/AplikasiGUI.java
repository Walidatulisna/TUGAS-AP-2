    private void cariiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariiActionPerformed
       if (pilihpencarian.getSelectedIndex() == 1) {

            try {
                selectByNim(Integer.parseInt(nim.getText()));
            } catch (Exception ex) {
                Logger.getLogger(AplikasiGUI.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else if (pilihpencarian.getSelectedIndex() == 2) {
            selectByNama(nama.getText());
        } else if (pilihpencarian.getSelectedIndex() == 3) {
            selectByAlamat(alamat.getText());
        } else if (pilihpencarian.getSelectedIndex() == 4) {

            String jeniskelamin = "";
            if (laki.isSelected()) {
                jeniskelamin = "L";

            } else if (perempuan.isSelected()) {
                jeniskelamin = "P";
            }
            selectByjk(jeniskelamin);

        } else {
            try {
                table();
            } catch (Exception ex) {

            }
        }
    
    
        
    }//GEN-LAST:event_cariiActionPerformed

