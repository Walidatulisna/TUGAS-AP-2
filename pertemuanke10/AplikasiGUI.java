    private void tabelmahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelmahasiswaMouseClicked
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) tabelmahasiswa.getModel(); //create a model
        int selectedRowIndex = tabelmahasiswa.getSelectedRow(); //get selected row
        int id = (int) dtm.getValueAt(selectedRowIndex, 0); //get the primary key to fetch data.

        koneksi konek = new koneksi();
        konek.koneksi();
        try {
            String sql = "select * from mahasiswa where nim like '%" + id + "%'";
            pst = konek.con.prepareStatement(sql);
            ResultSet rs = rs= pst.executeQuery(sql);

            while (rs.next()) {    //u can use normal query
                nim.setText (rs.getString("NIM"));
                nama.setText(rs.getString("NAMA"));
                alamat.setText(rs.getString("ALAMAT"));
                
                String jeniskelamin = rs.getString("Jenis_Kelamin");
                if (jeniskelamin.equals("L")){
                    laki.setSelected(true);
                }else{
                    perempuan.setSelected(true);
                }
            }
        } catch (Exception ex) {
            //   Logger.getLogger(ExistingStock.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tabelmahasiswaMouseClicked

